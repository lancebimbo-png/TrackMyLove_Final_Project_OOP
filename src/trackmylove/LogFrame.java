    private void loadDropdowns() {
        tml_dao.MemberDAO dao = new tml_dao.MemberDAO();

        java.util.List<tml_model.Member> patients = dao.findPatients(loggedMember.getHouseholdId());
        cmbPatient.removeAllItems();
        for (tml_model.Member m : patients) {
            cmbPatient.addItem(m.getFullName() + " [" + m.getRole() + "] id:" + m.getId());
        }

        java.util.List<tml_model.Member> caregivers = dao.findCaregivers(loggedMember.getHouseholdId());
        cmbGivenBy.removeAllItems();
        for (tml_model.Member m : caregivers) {
            cmbGivenBy.addItem(m.getFullName() + " [" + m.getRole() + "] id:" + m.getId());
        }
    }


    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // btnSave
        if (cmbPatient.getSelectedItem() == null) {
            tml_dao.InputValidator.showError(this, "Please select a patient.");
            return;
        }
        if (cmbGivenBy.getSelectedItem() == null) {
            tml_dao.InputValidator.showError(this, "Please select who is giving the medicine.");
            return;
        }
        if (!tml_dao.InputValidator.isValidDate(txtDate.getText().trim())) {
            tml_dao.InputValidator.showError(this, "Date must be in YYYY-MM-DD format.");
            return;
        }

        String patientStr = cmbPatient.getSelectedItem().toString();
        int patientId = Integer.parseInt(patientStr.split("id:")[1]);

        String giverStr = cmbGivenBy.getSelectedItem().toString();
        int giverId = Integer.parseInt(giverStr.split("id:")[1]);

        tml_model.MedicationLog log = new tml_model.MedicationLog();
        log.setPatientId(patientId);
        log.setGivenById(giverId);
        log.setLogDate(txtDate.getText().trim());
        log.setNotes(txtNotes.getText().trim());

        tml_dao.MedicationLogDAO logDAO = new tml_dao.MedicationLogDAO();
        int newLogId = logDAO.insert(log);

        if (newLogId == -1) {
            tml_dao.InputValidator.showError(this, "Failed to save log. Please try again.");
            return;
        }

        tml_dao.InputValidator.showSuccess(this, "Log saved! Now add the medications.");
        this.dispose();
        new trackmylove.LogDetailsFrame(loggedMember, newLogId).setVisible(true);
    }//GEN-LAST:event_btnSaveActionPerformed


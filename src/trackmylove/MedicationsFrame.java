    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        int row = jTable1.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a medication to edit.");
            return;
        }
        selectedMedId = (int) jTable1.getValueAt(row, 0);
        txtName.setText((String) jTable1.getValueAt(row, 1));
        txtDosage.setText(String.valueOf(jTable1.getValueAt(row, 2)));
        cmbUnit.setSelectedItem(jTable1.getValueAt(row, 3));
        txtPurpose.setText((String) jTable1.getValueAt(row, 4));
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int row = jTable1.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a medication to delete.");
            return;
        }
        int id = (int) jTable1.getValueAt(row, 0);
        String name = (String) jTable1.getValueAt(row, 1);
        if (tml_dao.InputValidator.confirmDelete(this, name)) {
            tml_dao.MedicationDAO dao = new tml_dao.MedicationDAO();
            boolean deleted = dao.delete(id);
            if (deleted) {
                tml_dao.InputValidator.showSuccess(this, name + " deleted successfully.");
                loadMedications();
            } else {
                tml_dao.InputValidator.showError(this, "Cannot delete — this medication is used in existing logs.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String name = txtName.getText().trim();
        String dosageStr = txtDosage.getText().trim();
        String purpose = txtPurpose.getText().trim();
        String notes = txtNotes.getText().trim();
        String unit = cmbUnit.getSelectedItem().toString();

        if (!tml_dao.InputValidator.isNotEmpty(name)) {
            tml_dao.InputValidator.showError(this, "Medicine name cannot be empty.");
            return;
        }
        if (!tml_dao.InputValidator.isValidDosage(dosageStr)) {
            tml_dao.InputValidator.showError(this, "Dosage must be a number greater than 0.");
            return;
        }
        if (!tml_dao.InputValidator.isNotEmpty(purpose)) {
            tml_dao.InputValidator.showError(this, "Purpose cannot be empty.");
            return;
        }

        tml_dao.MedicationDAO dao = new tml_dao.MedicationDAO();

        if (selectedMedId == -1) {
            if (dao.existsByName(name)) {
                tml_dao.InputValidator.showError(this, "That medication already exists.");
                return;
            }
            tml_model.Medication m = new tml_model.Medication();
            m.setName(name);
            m.setDosage(Double.parseDouble(dosageStr));
            m.setUnit(unit);
            m.setPurpose(purpose);
            m.setNotes(notes);
            dao.insert(m);
            tml_dao.InputValidator.showSuccess(this, "Medication added successfully.");
        } else {
            tml_model.Medication m = new tml_model.Medication();
            m.setId(selectedMedId);
            m.setName(name);
            m.setDosage(Double.parseDouble(dosageStr));
            m.setUnit(unit);
            m.setPurpose(purpose);
            m.setNotes(notes);
            dao.update(m);
            tml_dao.InputValidator.showSuccess(this, "Medication updated successfully.");
        }
        clearForm();
        loadMedications();
    }//GEN-LAST:event_btnSaveActionPerformed


    private void loadMedications() {
        tml_dao.MedicationDAO dao = new tml_dao.MedicationDAO();
        java.util.List<tml_model.Medication> list = dao.findAll();
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Name");
        model.addColumn("Dosage");
        model.addColumn("Unit");
        model.addColumn("Purpose");
        for (tml_model.Medication m : list) {
            model.addRow(new Object[]{
                m.getId(),
                m.getName(),
                m.getDosage(),
                m.getUnit(),
                m.getPurpose()
            });
        }
        jTable1.setModel(model);
    }


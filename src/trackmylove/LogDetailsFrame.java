    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

if (cmbMedication.getSelectedItem() == null) {
    tml_dao.InputValidator.showError(this, "Please select a medication.");
    return;
}
if (!tml_dao.InputValidator.isValidTime(txtTime.getText().trim())) {
    tml_dao.InputValidator.showError(this, "Time must be in HH:MM format.");
    return;
}

tml_model.Medication med = (tml_model.Medication) cmbMedication.getSelectedItem();
tml_dao.LogDetailDAO dao = new tml_dao.LogDetailDAO();

if (dao.alreadyLogged(currentLogId, med.getId())) {
    tml_dao.InputValidator.showError(this, "That medication is already in this log.");
    return;
}

tml_model.LogDetail detail = new tml_model.LogDetail();
detail.setLogId(currentLogId);
detail.setMedicationId(med.getId());
detail.setTimeGiven(txtTime.getText().trim());
detail.setWasTaken(cmbWasTaken.getSelectedItem().toString().equals("Yes"));
detail.setSideEffects(txtSideEffects.getText().trim());

boolean saved = dao.insert(detail);
if (saved) {
    tml_dao.InputValidator.showSuccess(this, "Medication added to log.");
    clearForm();
    loadDetails();
} else {
    tml_dao.InputValidator.showError(this, "Failed to add medication.");
}
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

    int row = jTable1.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a row to delete.");
        return;
    }
    int id = (int) jTable1.getValueAt(row, 0);
    String medName = (String) jTable1.getValueAt(row, 1);
    if (tml_dao.InputValidator.confirmDelete(this, medName)) {
        tml_dao.LogDetailDAO dao = new tml_dao.LogDetailDAO();
        boolean deleted = dao.delete(id);
        if (deleted) {
            tml_dao.InputValidator.showSuccess(this, medName + " removed from log.");
            loadDetails();
        } else {
            tml_dao.InputValidator.showError(this, "Failed to delete.");
        }
    }

    }//GEN-LAST:event_btnDeleteActionPerformed


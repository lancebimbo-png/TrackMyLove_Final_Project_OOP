private void loadMedications() {
    tml_dao.MedicationDAO dao = new tml_dao.MedicationDAO();
    java.util.List<tml_model.Medication> list = dao.findAll();
    cmbMedication.removeAllItems();
    for (tml_model.Medication m : list) {
        cmbMedication.addItem(m.getName() + " " + m.getDosage() + m.getUnit() + " id:" + m.getId());
    }
}

private void loadDetails() {
    tml_dao.LogDetailDAO dao = new tml_dao.LogDetailDAO();
    java.util.List<tml_model.LogDetail> list = dao.findByLog(currentLogId);
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Medication");
    model.addColumn("Time Given");
    model.addColumn("Status");
    model.addColumn("Side Effects");
    for (tml_model.LogDetail d : list) {
        model.addRow(new Object[]{
            d.getId(),
            d.getMedicationName(),
            d.getTimeGiven(),
            d.isWasTaken() ? "Taken" : "Skipped",
            d.getSideEffects()
        });
    }
    jTable1.setModel(model);
}

private void clearForm() {
    cmbMedication.setSelectedIndex(0);
    txtTime.setText("");
    cmbWasTaken.setSelectedIndex(0);
    txtSideEffects.setText("");
}

